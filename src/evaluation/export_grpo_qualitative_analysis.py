"""Report-oriented GRPO qualitative summary from metrics + file pointers."""
from __future__ import annotations

import argparse
import json
from pathlib import Path

from src.evaluation.compare_sft_dpo import _strict_rate
from src.utils.path_utils import find_project_root, resolve_path


def _load(root: Path, rel: str) -> dict:
    with open(resolve_path(rel, root), encoding="utf-8") as f:
        return json.load(f)


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--out", type=str, default="outputs/report_assets/grpo_qualitative_analysis.md")
    args = p.parse_args()
    root = find_project_root()
    out = resolve_path(args.out, root)
    out.parent.mkdir(parents=True, exist_ok=True)

    m1 = _load(root, "outputs/metrics/grpo_v1_test_metrics.json")
    m4 = _load(root, "outputs/metrics/grpo_v4_test_metrics.json")
    sft = _load(root, "outputs/metrics/sft_test_metrics.json")

    s1, s4 = _strict_rate(m1) or 0, _strict_rate(m4) or 0
    ss = _strict_rate(sft) or 0

    body = f"""# GRPO qualitative analysis (Part III)

Numbers below come from **test split greedy evaluation** (`*_test_metrics.json`), same decode settings as SFT.

## 1. GRPO-V1 behavior

- **ROUGE-L** ≈ {float(m1.get('rouge_l_f1', 0)):.4f} vs SFT ≈ {float(sft.get('rouge_l_f1', 0)):.4f}.
- **strict_format_rate** ≈ {s1:.3f} vs SFT ≈ {ss:.3f}.
- **avg_reward (v1)** ≈ {float(m1.get('avg_reward', 0)):.4f} (training objective proxy on test completions).

V1 rewards **tag presence** only. Models can earn high reward with **repeated `[point]/[reason]/[summary]` blocks** or **shallow text**, which **loose format** may still score as passing — **strict format** is needed to expose that failure mode.

## 2. GRPO-V4 behavior

- **ROUGE-L** ≈ {float(m4.get('rouge_l_f1', 0)):.4f}.
- **strict_format_rate** ≈ {s4:.3f}.
- **avg_reward (v4)** ≈ {float(m4.get('avg_reward', 0)):.4f}.

V4 adds ordering, numbered-reason heuristics, length/repetition shaping, and token overlap with `answer_en`. It **reduces some V1 hacks** but is **not identical** to strict evaluation and can still be gamed (e.g. keyword overlap without good summarization).

## 3. Reward hacking: V1 vs V4

- **V1** is intentionally **easy to hack** for the assignment: maximize tags with minimal semantic work.
- **V4** typically lowers reward on **tag-soup** and **extreme repetition**, but **cannot guarantee** human-quality summaries.

See concrete rows in:

- `outputs/report_assets/grpo_v1_reward_hacking_cases.md`
- `outputs/report_assets/grpo_v4_reward_hacking_cases.md`

## 4. Success vs failure (how to read generations)

- **Success-like**: single template, 2+ numbered reasons, concise `[summary]`, ROUGE-L not collapsed vs `summary_en_chosen`, **strict pass**.
- **Failure-like**: multiple full templates, empty reasons, or high reward with **strict fail** / low ROUGE-L.

## 5. Did strict metric help?

**Yes, as an external audit:** loose format stayed high for broken DPO runs earlier; strict format surfaced **duplicate legal blocks**. The same lens should be applied to GRPO outputs even though **RL optimized V1/V4 rewards, not strict**.

---

_Generated by `python -m src.evaluation.export_grpo_qualitative_analysis`._
"""
    out.write_text(body, encoding="utf-8")
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
