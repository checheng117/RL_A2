"""Report-style unified narrative for SFT / DPO / GRPO (Part IV alignment themes)."""
from __future__ import annotations

import argparse
import json

from src.evaluation.compare_sft_dpo import _strict_rate
from src.utils.path_utils import find_project_root, resolve_path


def _load(root, rel: str) -> dict:
    with open(resolve_path(rel, root), encoding="utf-8") as f:
        return json.load(f)


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--out", type=str, default="outputs/report_assets/unified_alignment_analysis.md")
    args = p.parse_args()
    root = find_project_root()
    out = resolve_path(args.out, root)
    out.parent.mkdir(parents=True, exist_ok=True)

    sft = _load(root, "outputs/metrics/sft_test_metrics.json")
    dpo = _load(root, "outputs/metrics/dpo_retune_v2_test_metrics.json")
    g1 = _load(root, "outputs/metrics/grpo_v1_test_metrics.json")
    g4 = _load(root, "outputs/metrics/grpo_v4_test_metrics.json")

    def line(name: str, m: dict) -> str:
        return (
            f"| {name} | {float(m.get('rouge_l_f1', 0)):.4f} | "
            f"{(_strict_rate(m) or 0):.3f} | {float(m.get('avg_output_length_tokens', 0)):.1f} |"
        )

    body = f"""# Unified alignment analysis (SFT, DPO, GRPO)

English-only structured summarization (`[point]` / `[reason]` / `[summary]`), random 90/5/5 split.

## 1. Overall comparison

| Paradigm | ROUGE-L | strict | len (tok) |
|----------|---------|--------|-----------|
{line("SFT", sft)}
{line("DPO retune v2", dpo)}
{line("GRPO-V1", g1)}
{line("GRPO-V4", g4)}

- **SFT** fits the reference distribution via supervised CE on `summary_en_chosen`.
- **DPO retune v2** aligns preferences from chosen/rejected pairs; in this project it can **match or slightly exceed** SFT on aggregate test metrics after conservative tuning.
- **GRPO-V1** optimizes a **weak** proxy (tags): good for demonstrating **misalignment** and hacking.
- **GRPO-V4** is a **stronger** proxy (structure + overlap with source) but still **not** full human judgment.

## 2. Why V1 is easier to hack

V1 = average of three booleans (tags present). Any completion containing `[point]`, `[reason]`, `[summary]` in any order of appearance in the string still gives **full tag credit** for `_tags_present` (substring check). Repeated blocks do not reduce V1. Therefore **reward hacking** is almost **by design** for Part III pedagogy.

## 3. Why V4 mitigates but does not solve hacking

V4 builds on order, numbered reasons (heuristic), length/repetition penalties, and overlap with `answer_en`. That blocks **some** degenerate policies but:

- Overlap can reward **copying** or **keyword stuffing**.
- Strict **single-template** constraints are **not** encoded in V4.
- **Strict format** in evaluation is stricter than the reward’s numbered-reason check in edge cases.

So V4 is **better aligned** than V1 with summary quality proxies, yet **reward hacking cases can remain** (see per-model hacking markdown).

## 4. Loose vs strict format — lesson for the whole project

**Loose** format (legacy) tracked whether tags existed and coarse order; **repeated legal templates** could still look “perfect.” **Strict** format enforces **exactly one** of each section and numbered reasons inside `[reason]`, matching the assignment’s intended single response. For any RL stage, **always report both**: optimization targets loose proxies; **strict** reveals silent regressions.

## 5. Three alignment paradigms (trade-offs)

1. **Supervised (SFT):** Stable, directly targets reference summaries; risk of **exposure bias** / limited exploration.
2. **Preference (DPO):** Can improve concision/format if pairs are clean; **bad rejects** or **too-strong β** can destroy quality (seen in original DPO before retune).
3. **Online RL (GRPO):** Flexible reward engineering; **high variance**, **reward hacking**, and need for **constraints** (length, format audits) and **sensible initialization** (here: **merged SFT** + small LoRA for GRPO).

## 6. Honesty clause

If GRPO **underperforms** SFT or DPO on ROUGE-L or strict on this split, that is still a **valid result**: the point of Part III is often to **show** how reward choice changes behavior and where metrics disagree. Do not hide worse numbers; explain them with **reward definition** and **hacking examples**.

---

_Table source: `unified_part34_metrics.md`. Generated by `python -m src.evaluation.export_unified_alignment_analysis`._
"""
    out.write_text(body, encoding="utf-8")
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
